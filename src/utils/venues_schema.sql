-- 1. Buat Tabel Venues (GOR)
CREATE TABLE IF NOT EXISTS public.venues (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  name TEXT NOT NULL,
  location TEXT NOT NULL,
  city TEXT NOT NULL,
  price_per_hour INTEGER NOT NULL,
  rating DECIMAL(2,1) DEFAULT 0.0,
  image_url TEXT,
  category TEXT DEFAULT 'Badminton',
  features TEXT[] DEFAULT '{}',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- 2. Aktifkan RLS agar data aman
ALTER TABLE public.venues ENABLE ROW LEVEL SECURITY;

-- 3. Kebijakan agar semua orang bisa melihat daftar lapangan
CREATE POLICY "Izinkan Semua Orang Melihat Venue" ON public.venues FOR SELECT USING (true);

-- 4. Buat Tabel Courts (Sub-lapangan di dalam GOR)
CREATE TABLE IF NOT EXISTS public.courts (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  venue_id BIGINT REFERENCES public.venues(id) ON DELETE CASCADE,
  name TEXT NOT NULL,
  type TEXT DEFAULT 'Vinyl',
  is_indoor BOOLEAN DEFAULT true,
  image_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.courts ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Izinkan Semua Orang Melihat Court" ON public.courts FOR SELECT USING (true);

