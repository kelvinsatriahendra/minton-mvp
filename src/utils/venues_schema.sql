-- 6. Buat Tabel Matches (Main Bareng)
CREATE TABLE IF NOT EXISTS public.matches (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  venue_name TEXT NOT NULL,
  city TEXT NOT NULL,
  match_date DATE NOT NULL,
  start_time TIME NOT NULL,
  end_time TIME NOT NULL,
  skill_level TEXT DEFAULT 'Bebas',
  price_per_person INTEGER DEFAULT 0,
  gender_requirement TEXT DEFAULT 'Campur',
  image_url TEXT,
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.matches ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Izinkan Semua Orang Melihat Match" ON public.matches FOR SELECT USING (true);

-- 7. Buat Tabel Bookings (Riwayat Pemesanan)
CREATE TABLE IF NOT EXISTS public.bookings (
  id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  user_email TEXT NOT NULL,
  venue_name TEXT NOT NULL,
  court_name TEXT NOT NULL,
  booking_date DATE NOT NULL,
  start_time TIME NOT NULL,
  end_time TIME NOT NULL,
  total_price INTEGER NOT NULL,
  status TEXT DEFAULT 'Lunas',
  created_at TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

ALTER TABLE public.bookings ENABLE ROW LEVEL SECURITY;
CREATE POLICY "User bisa melihat booking miliknya sendiri" ON public.bookings FOR SELECT USING (true); -- Untuk MVP kita izinkan dulu

